//Packing of numbers
//Given input: R1=0x00000012, R2=0x00000015, R3=0x00000016, R4=0x00000017
//Output: R5=0x17161512

   AREA ADDITION,CODE,READONLY
   ENTRY
   MOV R0,#0X40000010
   MOV R1,#0X00000012
   MOV R2,#0X00000015
   MOV R3,#0X00000016
   MOV R4,#0X00000017
   LSL R6,R4,#24        //R6=0x17000000
   LSL R7,R3,#16        //R7=0x00160000
   LSL R8,R2,#8         //R9=0x00001500
   ORR R9,R6,R7         //R9=0x17160000
   ORR R10,R8,R1        //R10=0x00001512
   ORR R5,R10,R9        //R5=0x17161512
L  B L
   END